    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        //updates the prices everytime the component is shown
        double totalTax;
        double selectedSeatPrice;
        double cash;
        String ticketChoosen = "none";

        selectedSeatPrice = PanelSeats.totalPayment;
        cash = AppFrame.currentLoggedCash;

        totalTax = selectedSeatPrice;
        discountPrice = PromptDiscount.totalDiscount * totalTax;

        totalPrice = totalTax - discountPrice;
        change = cash - totalPrice;

        //for ticket choosen
        if (PanelTickets.ticketChosed == 1) {
            ticketChoosen = "TALK-N-TEXT vs GLOBALPORT";
        } else if (PanelTickets.ticketChosed == 2) {
            ticketChoosen = "SAN MIGUEL vs KIA SORENTO";
        } else if (PanelTickets.ticketChosed == 3) {
            ticketChoosen = "MERALCO vs ALASKA";
        } else if (PanelTickets.ticketChosed == 4) {
            ticketChoosen = "PHOENIX vs NLEX";
        }

        //TIME TO UPDATE FILEDS
        fieldOrigPrice.setText(String.valueOf(totalTax));
        fieldDiscountedPrice.setText(String.valueOf(totalPrice));

        fieldCash.setText(String.valueOf(cash));
        fieldChange.setText(String.valueOf(change));

        lblTicketBooked.setText(ticketChoosen);

    }//GEN-LAST:event_formComponentShown

    private void btnMabuhayMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMabuhayMouseReleased
        // if change is not less than 0, then proceed
        if (change >= 0) {
            String pastHistory = getPastHistory();

            //for ticket choosen
            String ticketChoosen = "none";
            if (PanelTickets.ticketChosed == 1) {
                ticketChoosen = "TALK-N-TEXT vs GLOBALPORT";
            } else if (PanelTickets.ticketChosed == 2) {
                ticketChoosen = "SAN MIGUEL vs KIA SORENTO";
            } else if (PanelTickets.ticketChosed == 3) {
                ticketChoosen = "MERALCO vs ALASKA";
            } else if (PanelTickets.ticketChosed == 4) {
                ticketChoosen = "PHOENIX vs NLEX";
            }

            //fo user history
            //for date
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            currentDate = dtf.format(now);

            String entryHistory = "\n\n----------" + currentDate + "----------" + "\n" + ticketChoosen + "\n - Seat Price: " + PanelSeats.totalPayment
                    + "\n - Discount: " + PromptDiscount.totalDiscount + pastHistory;

            String sql = "update Users set cash = ?, history = ? where id = ?";
            PreparedStatement pst;

            try {
                pst = conn.prepareStatement(sql);
                pst.setDouble(1, change);
                pst.setString(2, entryHistory);
                pst.setInt(3, AppFrame.currentLoggedID);
                pst.executeUpdate();
                conn.close();
                pst.close();

            } catch (SQLException ex) {
                Logger.getLogger(PanelReceipt.class.getName()).log(Level.SEVERE, null, ex);
            }

            FrameReceipt frameReceipt = new FrameReceipt();
            
            frameReceipt.setLocation(this.getLocationOnScreen().x - 345, this.getLocationOnScreen().y - 50);
            frameReceipt.setVisible(true);

            AppFrame.cardLayout.show(this.getParent(), "cardCalculator");
        }
        
        //else, add some money first
        else {
            JOptionPane.showMessageDialog(this, "Oops! Your money is insufficient. Add some money first!");
            AppFrame.cardLayout.show(this.getParent(), "cardTransaction");
        }


    }//GEN-LAST:event_btnMabuhayMouseReleased


