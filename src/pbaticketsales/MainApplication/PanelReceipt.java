    private String getPastHistory() {
        ResultSet rs = null;
        String sql = "Select history from Users where id = ?";
        PreparedStatement pst;
        String info = "";

        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, AppFrame.currentLoggedID);
            rs = pst.executeQuery();

            info = rs.getString("history");

        } catch (SQLException ex) {
            Logger.getLogger(PanelReceipt.class.getName()).log(Level.SEVERE, null, ex);
        }

        return info;

    }

    private void loadHeader() {

        dropDown.setVisible(false);

        accountTab.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                ImageIcon accountHovered = new ImageIcon(getClass().getResource("/images/dropdown/AccountBtnRed.png"));
                accountTab.setIcon(accountHovered);
            }

            @Override
            public void mouseExited(MouseEvent e) {
                ImageIcon accountExited = new ImageIcon(getClass().getResource("/images/dropdown/AccountBtnGray.png"));
                if (!dropDown.isVisible()) {
                    accountTab.setIcon(accountExited);
                }
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                ImageIcon accountHovered = new ImageIcon(getClass().getResource("/images/dropdown/AccountBtnRed.png"));
                accountTab.setIcon(accountHovered);

                if (!dropDown.isVisible()) {
                    dropDown.setVisible(true);
                } else {
                    dropDown.setVisible(false);
                }
            }

        });

        dropDown.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                ImageIcon dropHistoryHovered = new ImageIcon(getClass().getResource("/images/dropdown/DropDownHistorySelect.png"));
                ImageIcon dropLogoutHovered = new ImageIcon(getClass().getResource("/images/dropdown/DropDownLogoutSelect.png"));

                dropDown.addMouseMotionListener(new MouseMotionAdapter() {
                    @Override
                    public void mouseMoved(MouseEvent e) {
                        if (e.getY() < 20) {
                            dropDown.setIcon(dropHistoryHovered);
                        } else {
                            dropDown.setIcon(dropLogoutHovered);
                        }
                    }

                });

            }

            @Override
            public void mouseExited(MouseEvent e) {

                ImageIcon dropExited = new ImageIcon(getClass().getResource("/images/dropdown/DropDown.png"));
                dropDown.setIcon(dropExited);
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                if (e.getY() < 20) {
                    FrameHistory frameHistory = new FrameHistory();

                    frameHistory.setVisible(true);
                } else {
                    LoginFrame loginFrame = new LoginFrame();
                    loginFrame.setVisible(true);

                    JFrame thisFrame = (JFrame) SwingUtilities.getWindowAncestor(getComponent(0));
                    thisFrame.dispose();

                }

                dropDown.setVisible(false);
            }

        });

    }
